const router = require('express').Router();
const { Users, NFT, Projects } = require('../../models');

// get all users
router.get('/', (req, res) => {
  Users.findAll({
    attributes: { exclude: ['password'] }
  })
    .then(dbUserData => res.json(dbUserData))
    .catch(err => {
      console.log(err);
      res.status(500).json(err);
    });
});

router.get('/:id', (req, res) => {
  Users.findOne({
    attributes: { exclude: ['password'] },
    where: {
      id: req.params.id
    },
    include: [
      {
        model: NFT,
        attributes: ['id', 'name', 'imageurl', 'projects_name', 'users_name', 'created_at']
      },
      {
        model: Projects,
        attributes: ['id', 'name', 'year', 'created_at'],
        include: {
          model: NFT,
          attributes: ['projects_name']
        }
      },
    ]
  })
    .then(dbUserData => {
      if (!dbUserData) {
        res.status(404).json({ message: 'No user found with this id' });
        return;
      }
      res.json(dbUserData);
    })
    .catch(err => {
      console.log(err);
      res.status(500).json(err);
    });
});

